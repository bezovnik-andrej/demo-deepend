/* ── Seed demo markup values ──────────────────────────────────────────────
   Per-item dollar markup is a new concept on the data model. Rather than
   editing every entry above, we attach a realistic default (22% of price,
   rounded, with a $1 floor) to every leaf that doesn't already declare one.
   Parent/container nodes (price=0) keep markup=0. Once the app persists
   user-edited markups, those round-trip through the reducer untouched. */
function seedMarkup(items: ProjectItem[]): ProjectItem[] {
  return items.map((item) => ({
    ...item,
    markup:
      item.markup !== undefined
        ? item.markup
        : item.price > 0
          ? Math.max(1, Math.round(item.price * 0.22))
          : 0,
    children: item.children ? seedMarkup(item.children) : item.children,
  }));
}

export const DEFAULT_PROJECT_ITEMS: ProjectItem[] = seedMarkup(RAW_DEFAULT_PROJECT_ITEMS);

/** Flatten the tree for BOM table view */
export function flattenItems(items: ProjectItem[]): ProjectItem[] {
  const result: ProjectItem[] = [];
  for (const item of items) {
    if (item.children && item.children.length > 0) {
      result.push(...flattenItems(item.children));
    } else if (item.partNo) {
      result.push(item);
    }
  }
  return result;
}

/** Calculate subtotal for a node (recursive) */
export function subtotal(node: ProjectItem): number {
  if (node.children && node.children.length > 0) {
    return node.children.reduce((s, c) => s + subtotal(c), 0);
  }
  return node.qty * node.price;
}

/** Deep clone for editable BOM tree state */
export function cloneProjectItems(items: ProjectItem[]): ProjectItem[] {
  return structuredClone(items);
}

/** Count purchasable leaf lines under a node */
export function countLeafLines(node: ProjectItem): number {
  if (node.children?.length) {
    return node.children.reduce((s, c) => s + countLeafLines(c), 0);
  }
  return node.partNo ? 1 : 0;
}
